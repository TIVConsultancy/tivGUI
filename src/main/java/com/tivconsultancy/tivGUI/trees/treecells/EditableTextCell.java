package com.tivconsultancy.tivGUI.trees.treecells;

import com.tivconsultancy.tivGUI.trees.treecells.contentclasses.TreeContentClass;
import javafx.event.EventHandler;
import javafx.scene.control.TextField;
import javafx.scene.control.TreeCell;
import javafx.scene.input.KeyCode;
import javafx.scene.input.KeyEvent;

/**
 *
 * @author Thomas Ziegenhein
 */
public class EditableTextCell extends TreeCell<TreeContentClass> {
 
        private TextField textField;        
 
        public EditableTextCell() {
            createTextField();
        }
 
        @Override
        public void startEdit() {
            super.startEdit();
 
            if (textField == null) {
                createTextField();
            }
            setText(null);
            setGraphic(textField);
            textField.selectAll();
        }
 
        @Override
        public void cancelEdit() {
            super.cancelEdit();
            setText( ((TreeContentClass) getItem()).getDisplay());
            setGraphic(getTreeItem().getGraphic());
        }
 
        @Override
        public void updateItem(TreeContentClass item, boolean empty) {
            super.updateItem(item, empty);
 
            if (empty) {
                setText(null);
                setGraphic(null);
            } else {
                if (isEditing()) {
                    if (textField != null) {
                        textField.setText(getString());
                    }
                    setText(null);
                    setGraphic(textField);
                } else {
                    setText(getString());
                    setGraphic(getTreeItem().getGraphic());
                }
            }
        }
 
        private void createTextField() {
//            getItem().setValue(getString());
            textField = new TextField(getString());
            textField.setOnKeyReleased(new EventHandler<KeyEvent>() {
 
                @Override
                public void handle(KeyEvent t) {
                    if (t.getCode() == KeyCode.ENTER) {
                        System.out.println(textField.getText());
                        System.out.println(getString());
                        System.out.println(getItem());
                        commitEdit(getItem());
                    } else if (t.getCode() == KeyCode.ESCAPE) {
                        cancelEdit();
                    }
                }
            });
        }
 
        private String getString() {
            return getItem() == null ? "" : getItem().toString();
        }
    }
